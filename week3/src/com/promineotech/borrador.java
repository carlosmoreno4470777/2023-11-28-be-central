	private static void multidimensional(int a, int b) {

        // Check for error - both numbers need to be greater than 2
        if (a <= 2 || b <= 2) {
            System.out.println("Both numbers need to be greater than 2.");
            return; // Don't proceed if invalid parameters
        }

        // Declare the multidimensional array based on user inputs
        int[][] arr = new int[a][b];

        // Loop through each row (outer loop)
        for (int i = 0; i < a; i++) {

            // Loop through each column within the row (inner loop)
            for (int j = 0; j < b; j++) {

                // Store the current index value in the array
                arr[i][j] = j; // Alternatively, you can assign any desired value here
            }
        }

        // Print the last element of the first row using appropriate indexing
        System.out.println(arr[0][arr[0].length - 1]);
		
	}

	
	
